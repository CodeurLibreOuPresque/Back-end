// COUNT() retourne le nombre de ligne repondant à un critére
// AVG() retourne la moyenne des colonnes numerique selectionnées
// SUM() retourne le total des colonnes numeriques selectionnées

SELECT COUNT(column_name)
FROM table_name
WHERE condition;

SELECT AVG(column_name)
FROM table_name
WHERE condition;

SELECT SUM(column_name)
FROM table_name
WHERE condition;
